public class S205_ListShortReport {
	private String hinCD;
	private String hinName;
	private String shortReasonCD;
	private String shortReason;
	private String hour;

	public String getHinCD() {
		return hinCD;
	}

	public void setHinCD(String hinCD) {
		this.hinCD = hinCD;
	}

	public String getHinName() {
		return hinName;
	}

	public void setHinName(String hinName) {
		this.hinName = hinName;
	}
	
	public String getShortReasonCD() {
		return shortReasonCD;
	}

	public void setShortReasonCD(String shortReasonCD) {
		this.shortReasonCD = shortReasonCD;
	}

	public String getShortReason() {
		return shortReason;
	}

	public void setShortReason(String shortReason) {
		this.shortReason = shortReason;
	}

	public String getHour() {
		return hour;
	}

	public void setHour(String hour) {
		this.hour = hour;
	}

}
